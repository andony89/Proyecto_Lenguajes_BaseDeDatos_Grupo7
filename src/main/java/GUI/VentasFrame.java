    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try {
            // Obtener valores de los campos de texto
            Date fecha = Date.valueOf(txfFecha.getText()); // Convierte String a Date
            int cedulaID = Integer.parseInt(txfCedulaID.getText());
            double total = Double.parseDouble(txfTotal.getText());

            // Llamar al procedimiento almacenado para crear una nueva venta
            Connection conn = ConexionOracle.getConnection();
            CallableStatement cs = conn.prepareCall("{call CrearVenta(?, ?, ?)}");
            cs.setDate(1, fecha);
            cs.setInt(2, cedulaID);
            cs.setDouble(3, total);
            cs.execute();

            cs.close();
            conn.close();

            limpiarCampos();
            actualizarTabla();
            JOptionPane.showMessageDialog(this, "Venta agregada exitosamente.");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error al agregar la venta: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            int ventaID;
            try {
                ventaID = Integer.parseInt(txfVentaID.getText());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "ID de venta inválido. Asegúrese de ingresar un número válido.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            try (Connection conn = ConexionOracle.getConnection()) {
                String sql = "{call pkg_ventas.LeerVenta(?, ?, ?, ?)}";
                try (CallableStatement cs = conn.prepareCall(sql)) {
                    cs.setInt(1, ventaID);
                    cs.registerOutParameter(2, java.sql.Types.DATE);
                    cs.registerOutParameter(3, java.sql.Types.INTEGER);
                    cs.registerOutParameter(4, java.sql.Types.DOUBLE);
                    cs.execute();

                    limpiarTabla();
                    DefaultTableModel model = (DefaultTableModel) tableVentas.getModel();

                    Date fecha = cs.getDate(2);
                    if (fecha != null) {
                        model.addRow(new Object[]{
                            ventaID,
                            fecha,
                            cs.getInt(3),
                            cs.getDouble(4)
                        });
                    } else {
                        JOptionPane.showMessageDialog(this, "No se encontró la venta con el ID especificado.");
                    }
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error al buscar la venta: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

