    private void btnObtenerVentasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObtenerVentasActionPerformed
        Connection conn = null;
        CallableStatement cs = null;
        ResultSet rs = null;

        try {
            // Establecer la conexión con la base de datos
            conn = ConexionOracle.getConnection();

            // Preparar la llamada al procedimiento almacenado
            cs = conn.prepareCall("{call ObtenerVentas(?)}");

            // Registrar el parámetro de salida
            cs.registerOutParameter(1, OracleTypes.CURSOR);

            // Ejecutar el procedimiento
            cs.execute();

            // Obtener el cursor
            rs = (ResultSet) cs.getObject(1);

            // Limpiar la tabla
            limpiarTabla();

            // Llenar la tabla con los resultados
            DefaultTableModel model = (DefaultTableModel) tableVentas.getModel();
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("VentaID"),
                    rs.getDate("Fecha"),
                    rs.getInt("CedulaID"),
                    rs.getDouble("Total")
                });
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error al obtener las ventas: " + ex.getMessage());
        } finally {
            // Cerrar recursos
            try {
                if (rs != null) {
                    rs.close();
                }
            } catch (SQLException e) {
                /* Ignorar */ }
            try {
                if (cs != null) {
                    cs.close();
                }
            } catch (SQLException e) {
                /* Ignorar */ }
            try {
                if (conn != null) {
                    conn.close();
                }
            } catch (SQLException e) {
                /* Ignorar */ }
        }
    }//GEN-LAST:event_btnObtenerVentasActionPerformed

    /**
     * @param args the command line arguments
     */
    private void limpiarTabla() {
        DefaultTableModel model = (DefaultTableModel) tableVentas.getModel();
        model.setRowCount(0);
    }

    private void limpiarCampos() {
        txfVentaID.setText("");
        txfFecha.setText("");
        txfCedulaID.setText("");
        txfTotal.setText("");
    }

    private void actualizarTabla() {
        try {
            Connection conn = ConexionOracle.getConnection();
            Statement stmt = conn.createStatement();

            String query = "SELECT * FROM Ventas";
            ResultSet rs = stmt.executeQuery(query);

            DefaultTableModel model = (DefaultTableModel) tableVentas.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                int ventaID = rs.getInt("VentaID");
                Date fecha = rs.getDate("Fecha");
                int cedulaID = rs.getInt("CedulaID");
                double total = rs.getDouble("Total");

                model.addRow(new Object[]{ventaID, fecha, cedulaID, total});
            }

            rs.close();
            stmt.close();
            conn.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error al actualizar la tabla: " + ex.getMessage());
        }
    }

